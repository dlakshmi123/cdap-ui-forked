package com.continuuity.common.metrics;

/**
 * Class defines types of metrics allowed.
 */
public enum MetricType {
  /**
   * Metrics that are part of lower level system e.g. disk, i/o, network.
   */
  System,

  /**
   * Metrics that are collected as part of the flow system.
   */
  FlowSystem,

  /**
   * Metrics that are generated by the user.
   */
  FlowUser,

  /**
   * Metrics that are collected as part of the procedure runtime system.
   */
  ProcedureSystem,

  /**
   * Metrics that are generated by procedure user.
   */
  ProcedureUser;
}
